#include <stdio.h>

/**
 * add - adds two integers
 * @a: first integer parameter
 * @b: second integer parameter
 *
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substructs an integer from anoter
 * @a: first integer parameter
 * @b: second integer parameter
 *
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first integer parameter
 * @b: second integer parameter
 *
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: first integer parameter
 * @b: second integer parameter
 *
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds the modulo of two integers
 * @a: first integer parameter
 * @b: second integer parameter
 *
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
